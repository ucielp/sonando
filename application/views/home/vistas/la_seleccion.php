                        6 de Octubre
                        <h3><p>La Selección sub 22 goleó por 6 a 0 a Banco Nación</p></h3>
                        El Martes 6 de octubre se midió en condición de visitante frente a Banco Nación al principio del partido ambos elencos se repartían el balón, pero rápidamente los pibes de Soñando con el gol, ejercieron el control total del partido, generando jugada de gol, mediante tiro libres, a través de la participación de casi todos los futbolistas, con proyección de los laterales que provocaban centros, para que los delanteros lleguen al gol. Los 6 tantos fueron marcados por los 2 delanteros, el primer gol llego promediando la primera etapa, tras un pase de Lucas Mansilla, donde L Canteros, solo tuvo que empujar el balón, luego llegaron goles del mismo jugador, mediante un cabezazo potente y un tiro libre ejecutado con gran precisión.
</br>
