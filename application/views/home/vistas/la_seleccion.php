                        <h3><p>Gran Goleada del Seleccionado Sub 16 de Soñando con el Gol sobre Jockey Club</p></h3>
                        El martes 13 de octubre en las instalaciones del Jockey Club de Rosario, nuestro seleccionado jugo un nuevo partido amistoso y con un juego demoledor en todas las líneas goleó por 5 a 1 a Jockey Club de Rosario. El equipo mostro firmeza en todas las líneas de juego, donde se realizaron certeros pases y en todo momento se jugó con la proyección de los laterales y abriendo la cancha. Por otro lado ladefensa estuvo muy sólido y las únicas llegadas del rival se produjeron a través de la pelota parada.
</br>
La posición de la pelota fue controlado siempre por el seleccionado de Soñando con el gol, donde el contrincante jamás pudo hacerse de la pelota, se generaron una gran cantidad de jugadas de gol se llegó, siempre con una gran claridad. Los goles fueron anotados por: Nahuel Zapata, Kevin Fernandez, Elvio Fernández, Agustín Esquivel y David Burgues. En definitiva fue una tarde con un notable ritmo futbolístico y que pregona en el futuro un seleccionado con un eficiente volumen ofensivo. Los integrantes del plantel fueron : David Burgues, Elvio Fernanez, Kevin Fernandez, Javier Fernandez, Ignacio Lujambio ( La Plaza), Mario Sosa ( País Vasco), Vale Klun ( Wachipato), Nahuel Zapata ( Salsipuedes), Alan Mesa, Pedro Martinez ( Fantastico), Gabriel Minaglio, Agustin Esquivel ( River Plate), Patricio Solis ( U Américana), Facundo Alvarez ( Cocoon), Mateo Ramasco ( Cironga), Kevin Bostosetti ( Mezcladito Fc) Director Técnico: Francisco Pochettino, ayudante de campo: Luciando Olmedo, Preparador Físico: Lucas Fernandez:
